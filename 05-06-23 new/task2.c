#include<stdio.h>

int main(){
	
	char a;
	
	printf("Enter Value : ");
	scanf("%c",&a);
	
	switch(a){
		
		case 'J':
			printf("JANUARY");
			break;
			
			case 'F':
			printf("FEBRUARY");
			break;
			
			case 'M':
			printf("MARCH");
			break;
			
			case 'A':
			printf("APRIL");
			break;
			
			case 'Y':
			printf("MAY");
			break;
			
			case 'U':
			printf("JUNE");
			break;
			
			case 'L':
			printf("JULY");
			break;
			
			case 'G':
			printf("AUGUST");
			break;
			
			case 'S':
			printf("SEPTEMBER");
			break;
			
			case 'O':
			printf("OCTOBER");
			break;
			
			case 'N':
			printf("NOVEMBER");
			break;
			
			case 'D':
			printf("DECEMBER");
			break;
			
			
	}
	
	return 0;
}
